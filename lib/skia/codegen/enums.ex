defmodule Skia.Codegen.Enums do
  @moduledoc false

  alias RustQ.Rust
  alias RustQ.Rust.AST.TypeBuilder, as: T
  alias Skia.Codegen.SkiaSafe

  @specs %{
    blend_mode: [skia: "SkBlendMode"],
    stroke_cap: [skia: "SkPaint_Cap"],
    stroke_join: [skia: "SkPaint_Join"],
    fill_rule: [skia: "SkPathFillType"],
    path_op: [skia: "SkPathOp"],
    clip_op: [skia: "SkClipOp"],
    encoded_image_format: [skia: "SkEncodedImageFormat"],
    blur_style: [skia: "SkBlurStyle"],
    sampling: [skia: "SkFilterMode"],
    tile_mode: [skia: "SkTileMode"],
    mipmap_mode: [skia: "SkMipmapMode"]
  }

  @spec generated() :: String.t()
  def generated do
    entries =
      defs()
      |> Enum.sort_by(&elem(&1, 0))
      |> Enum.map(fn {name, spec} ->
        values = spec |> Keyword.fetch!(:variants) |> Enum.map(&elem(&1, 0))

        Rust.const(
          enum_const_name(name),
          T.ref(T.slice(T.ref(:str))),
          Rust.expr(enum_values(values)), vis: :pub)
      end)

    decoders =
      defs()
      |> Enum.sort_by(&elem(&1, 0))
      |> Enum.map(fn {name, spec} -> enum_decoder(name, spec) end)

    "generated_enums.rs"
    |> template_path()
    |> RustQ.render_file!(
      preamble: generated_rust_preamble(),
      splice: [entries: entries, decoders: decoders]
    )
  end

  @spec atoms() :: [atom()]
  def atoms do
    defs()
    |> Enum.flat_map(fn {_name, spec} ->
      spec |> Keyword.fetch!(:variants) |> Enum.map(&elem(&1, 0))
    end)
  end

  @spec defs() :: %{atom() => keyword()}
  def defs do
    Map.new(@specs, fn {name, _spec} -> {name, spec!(name)} end)
  end

  @spec spec!(atom()) :: keyword()
  def spec!(name), do: @specs |> Map.fetch!(name) |> resolve_spec()

  defp resolve_spec(spec) do
    descriptor =
      Keyword.get_lazy(spec, :descriptor, fn ->
        spec |> Keyword.fetch!(:skia) |> SkiaSafe.enum_descriptor!()
      end)

    spec
    |> Keyword.put(:descriptor, descriptor)
    |> Keyword.put(:rust, Skia.Codegen.NativeSchema.safe_enum_type!(descriptor.name))
    |> Keyword.put(:variants, RustQ.NativeEnumDescriptor.variants(descriptor))
  end

  defp enum_const_name(name) do
    rust_name = name |> Atom.to_string() |> Macro.camelize()
    "#{Macro.underscore(rust_name) |> String.upcase()}S"
  end

  defp enum_values(values) do
    values = Enum.map_join(values, ", ", &inspect(to_string(&1)))
    "&[#{values}]"
  end

  defp enum_decoder(name, spec) do
    RustQ.Rustler.atom_decoder("decode_#{name}",
      returns: Keyword.fetch!(spec, :rust),
      descriptor: Keyword.fetch!(spec, :descriptor)
    )
  end

  defp template_path(name) do
    __DIR__
    |> Path.join("../../../priv/codegen/templates/#{name}")
    |> Path.expand()
  end

  defp generated_rust_preamble do
    "// Generated by mix skia.codegen. Do not edit by hand.\n\n"
  end
end
